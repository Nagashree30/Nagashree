package com.xworkz.xworkzapp.tcasting;

public class IntToFloat {
	public static void main(String[] args) {
		int myInt=20;
		float myFloat = myInt; // Automatic casting: int to float

	    System.out.println(myInt);      
	    System.out.println(myFloat);  
	    
	    int myInt1 = 9;
	    double myDouble = myInt1; // Automatic casting: int to double

	    System.out.println(myInt1);      
	    System.out.println(myDouble); 
	    
	    int myInt3 = 990079477;
	    long myLong = myInt3; // Automatic casting: int to long

	    System.out.println(myInt3);      
	    System.out.println(myLong); 
		
	    byte myByte=124;
	    short myShort=myByte;
	    System.out.println(myByte);      
	    System.out.println(myShort); 
	    
	    
	    
	}

}
